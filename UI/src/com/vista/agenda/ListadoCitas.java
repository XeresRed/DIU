    private void construirCitas(ArrayList<Organizador> citasFiltradas) {
        int y = 75;
        int x = 0;
        int heig = 121;
        int wid = 510;

        listaPanelesCitas = new ArrayList<>();
        for (int i = 0; i < citasFiltradas.size(); i++) {
            final int iValue = i;
            panelCitas cita = new panelCitas(this,citasFiltradas.get(i),userAccedido,vista);
            listaPanelesCitas.add(cita);
            if(fe){
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                String fechaComoCadena = sdf.format(citasFiltradas.get(i).getFecha());
                cita.setTextFecha(fechaComoCadena);
            }else{
                cita.setTextFecha("");
            }
            cita.setVisible(true);
            jPanel1.add(cita, new org.netbeans.lib.awtextra.AbsoluteConstraints(x, y, wid, heig));
            y += heig+5;
        }
    }
